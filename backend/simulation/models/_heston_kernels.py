"""
Heston path-simulation Numba kernels (one per discretization scheme)
====================================================================

Split out of ``heston.py``: the former single kernel carried a per-step
``if scheme == ...`` chain over all four variance discretizations and a
uniform signature that forced dummy QE buffers onto the non-QE schemes.
Each scheme now compiles to its own specialized kernel — the scheme
branch leaves the hot loop, the non-QE kernels drop the two unused
``(n_paths, n_steps)`` buffers, and the QE kernel hoists its
``kappa``/``dt``-only subexpressions out of the step loop.

All random draws are pre-generated by the caller and passed via the
``z1_noise``/``z2_noise`` (and, for QE, ``qe_normal``/``qe_uniform``)
arrays. This makes the parallel kernels deterministic across runs for a
fixed seed and avoids Numba's thread-local RNG race conditions.

Author: Thomas Vaudescal
Created: 2026
"""

from __future__ import annotations

import numpy as np
from numba import njit, prange

from backend.utils.constants.numerical import QE_M_FLOOR, QE_PSI_THRESHOLD

# Module-level aliases captured by Numba at compile time.
_M_FLOOR = QE_M_FLOOR
_QE_PSI_THRESHOLD = QE_PSI_THRESHOLD


@njit(parallel=True, cache=True, fastmath=True)
def heston_paths_euler(
    s0: float,
    v0: float,
    mu: float,
    kappa: float,
    theta: float,
    alpha: float,
    rho: float,
    t: float,
    n_paths: int,
    n_steps: int,
    z1_noise: np.ndarray,
    z2_noise: np.ndarray,
) -> tuple[np.ndarray, np.ndarray]:
    """Simple Euler scheme (variance can go negative)."""
    dt = t / n_steps
    sqrt_dt = np.sqrt(dt)
    sqrt_one_minus_rho2 = np.sqrt(1.0 - rho * rho)

    s_paths = np.empty((n_paths, n_steps + 1), dtype=np.float64)
    v_paths = np.empty((n_paths, n_steps + 1), dtype=np.float64)

    for i in prange(n_paths):
        s_paths[i, 0] = s0
        v_paths[i, 0] = v0

        for j in range(n_steps):
            z1 = z1_noise[i, j]
            z2 = z2_noise[i, j]

            dw_s = sqrt_dt * z1
            dw_v = sqrt_dt * (rho * z1 + sqrt_one_minus_rho2 * z2)

            v_curr = v_paths[i, j]
            s_curr = s_paths[i, j]

            sqrt_v = np.sqrt(max(v_curr, 0.0))
            v_next = v_curr + kappa * (theta - v_curr) * dt + alpha * sqrt_v * dw_v
            s_next = s_curr * np.exp((mu - 0.5 * v_curr) * dt + sqrt_v * dw_s)

            v_paths[i, j + 1] = v_next
            s_paths[i, j + 1] = s_next

    return s_paths, v_paths


